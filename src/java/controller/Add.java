    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String code = request.getParameter("code");
        String name = request.getParameter("name");
        String carriages_raw = request.getParameter("carriages");
        int carriages;
        HttpSession session = request.getSession();
        List<Student> list = (List<Student>)session.getAttribute("list");
        if (list == null) {
            list = new ArrayList<>();
        }
        try {
            carriages = Integer.parseInt(carriages_raw);
            Student s = new Student(code, name, carriages);
            list.add(s);
            session.setAttribute("list", list);
            request.getRequestDispatcher("myexam.jsp").forward(request, response);
        } catch (NumberFormatException e) {
        }
    }

